# Order of execution  PEMDAS (Parenthesis, Exponents, Multiplication, Division, Addition, Substraction) 
# The actual order is you do the multiplication and division in one step, from left to right, then you do the addition and substraction in one step
# from left to right
# can also be re written as PE(M&D)(A&S)